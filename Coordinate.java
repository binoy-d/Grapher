public class Coordinate{
  
  //A very self-explanatory class
  double x;
  double y;
  public Coordinate(double x, double y){
    this.x = x;
    this.y = y;
  }
  public Coordinate(){
    this.x = 0;
    this.y = 0;
  }
  public double getX(){
    return this.x;
  }
  public double getY(){
    return this.y;
    
  }
  
}
